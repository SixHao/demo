        $("#ldiv").on('mouseover', function(){
            // console.log(111);
            $("#rdiv").css("display", 'block');
            $("#move").css({display:'block'});
            $(this).css('cursor', 'move');

            // 获取右侧 div 的宽度高度。
            var rW = $("#rdiv").width();
            var rH = $("#rdiv").height();

            // 获取右侧 rimg 的宽度和高度。
            var rImgW = $("#rimg").width();
            var rImgH = $("#rimg").height();

            // 计算比例。
            var mX = rW/rImgW;
            var mY = rH/rImgH;

            // 获取左侧 div 的宽度和高度。
            lW = $("#ldiv").width();
            lH = $("#ldiv").height();

            // 计算 move 的宽度和高度。
            mW = lW*mX;
            mH = lH*mY;
            // 设置 move 的宽度和高度。
            $("#move").css({width:mW+'px',height:mH+'px'});
        });

        $("#ldiv").on("mousemove", function(e){

            $("#move").css('cursor', 'move');
            // 获取鼠标坐标。
            var mouseX = e.pageX;
            var mouseY = e.pageY;

            // 获取 ldiv的偏移量。
            var ldivLeft = $("#ldiv").offset().left;
            // console.log(ldivLeft);
            var ldivTop = $("#ldiv").offset().top;

            // 计算。
            var mLeft = mouseX - ldivLeft - mW/2;
            var mTop = mouseY - ldivTop - mH/2;

            // 判断。
            if(mLeft <= 0)
            {
                mLeft = 0;
            }

            if(mTop <= 0)
            {
                mTop = 0;
            }

            if(mLeft >= lW-mW)
            {
                mLeft = lW-mW;
            }

            if(mTop >= lH-mH)
            {
                mTop = lH-mH;
            }

            
            // 设置move 的 left top
            $("#move").css({left:mLeft+ 'px', top: mTop+ 'px'});

            // 计算大图移动 的比例。
            var rImgLP = mLeft/lW;
            var rImgTP = mTop/lH;

            // 获取大图的宽度和高度。
            var rImgW = $("#rimg").width();
            var rImgH = $("#rimg").height();

            var rImgLeft = rImgW*rImgLP;
            var rImgTop = rImgH*rImgTP;

            // 设置大图。
            $("#rimg").css({left:-rImgLeft + 'px',top:-rImgTop + 'px'});

        });


        $("#ldiv").on("mouseout", function(e){
            // console.log(222);
            // 让 move 隐藏。
            $("#move").css({display:'none'});
            $("#rdiv").css({display:'none'});

            $(this).css('cursor', 'default');
        });

